package com.lei.h5.model;


public class StatisticLog {
	private String type;
	private int count;
	private String logDay;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getLogDay() {
		return logDay;
	}
	public void setLogDay(String logDay) {
		this.logDay = logDay;
	}
	
}
